    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String message = "";
        try {
            String customername = jtfCustomerName.getText().trim();
            String colonyName = jtfColonyName.getText().trim();
            String address = jtfAddress.getText().trim();
            String contactNo = jtfContactNo.getText().trim();
            String noOfCylinder = jtfNoOfCylinder.getText().trim();
            if (Checks.isEmpty(customername) || Checks.isEmpty(colonyName) || Checks.isEmpty(address) || Checks.isEmpty(contactNo) || Checks.isEmpty(noOfCylinder)) {
                message = "Please Fill All Boxes";
            } else if (!(jcbCylinderType.getSelectedItem() instanceof CylinderTypeTO)) {
                message = "Please Choose Any Cylinder Type";
            } else if (Checks.isNumeric(noOfCylinder)) {
                CylinderTypeTO ctt = (CylinderTypeTO) jcbCylinderType.getSelectedItem();
                CustomerInfoTO data = new CustomerInfoTO();
                data.setCustomerName(customername);
                data.setColonyName(colonyName);
                data.setAddress(address);
                data.setContactNo(contactNo);
                data.setTypeID(ctt.getTypeID());
                data.setNoOfCylinder(Integer.parseInt(noOfCylinder));
                CustomerInfoDAO action = new CustomerInfoDAO();
                if (btnAdd.getText().contains("Add")) {
                    if (action.insertRecord(data)) {
                        message = " Customer Info is Added in the System";
                    } else {
                        message = action.getErrorMessage();
                    }
                } else if (btnAdd.getText().contains("Update")) {
                    if (updateData != null) {
                        data.setCustomerID(updateData.getCustomerID());
                        if (action.updateRecord(data)) {
                            message = " Customer Info is Updated in the System";
                            ViewCustomerInfo obj = new ViewCustomerInfo();
                            obj.setVisible(true);
                            JDesktopPane jdp = getDesktopPane();
                            jdp.add(obj);
                            try {
                                obj.setSelected(true);
                            } catch (PropertyVetoException ex) {
                            }
                            dispose();
                        } else {
                            message = action.getErrorMessage();
                        }
                    } else {
                        message = " Not Allowed Operation ";
                    }
                }
            } else {
                message = "Please Enter Numeric Value in No of Cylinder Box";
            }
        } catch (Exception ex) {
            message = ex.getMessage();
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_btnAddActionPerformed
