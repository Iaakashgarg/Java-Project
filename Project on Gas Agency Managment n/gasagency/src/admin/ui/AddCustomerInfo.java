    public AddCustomerInfo(CustomerInfoTO data) {
        this();
        this.updateData = data;
        if (data != null) {
            btnAdd.setText("Update Customer Info");
            jtfCustomerName.setText(data.getCustomerName());
            jtfColonyName.setText(data.getColonyName());
            jtfContactNo.setText(data.getContactNo());
            jtfAddress.setText(data.getAddress());
            jtfNoOfCylinder.setText(String.valueOf(data.getNoOfCylinder()));
            for (int i = 1; i <= jcbCylinderType.getItemCount(); i++) {
                CylinderTypeTO ctt = (CylinderTypeTO) jcbCylinderType.getItemAt(i);
                if (ctt.getTypeID().equalsIgnoreCase(updateData.getTypeID())) {
                    jcbCylinderType.setSelectedIndex(i);
                    break;
                }
            }
        }
    }

    private void bindCylinderType() {
        jcbCylinderType.removeAllItems();
        jcbCylinderType.addItem("Select A Type");
        ArrayList<CylinderTypeTO> allTypes = new CylinderTypeDAO().getAllRecord();
        if (allTypes != null) {
            for (CylinderTypeTO ctt : allTypes) {
                jcbCylinderType.addItem(ctt);
            }
        }
    }

